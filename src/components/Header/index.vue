<script lang="ts">
import { Component, Prop, Vue, Watch } from 'vue-property-decorator';
import Menu from '@/components/Menu';
import { ipcRenderer, remote } from 'electron';
import { MAIN_MIN, MAIN_ZOOM, MAIN_CLOSE } from '@/constant/ipc';
import { get_search_suggest } from '@/actions';
import Drawer from '@/components/Drawer';
@Component({
  components: { Menu, Drawer },
})
export default class HelloWorld extends Vue {
  public visible = false;
  public keywords = '';
  public seachList = {};
  public types = {
    artists: {
      name: 'Ê≠åÊâã',
      icon: 'icon-account',
    },
    songs: {
      name: 'Ê≠åÊõ≤',
      icon: 'icon-music-note',
    },
    albums: {
      name: '‰∏ìËæë',
      icon: 'icon-music-circle',
    },
    mvs: {
      name: 'ËßÜÈ¢ë',
      icon: 'icon-youtube-play',
    },
    playlists: {
      name: 'Ê≠åÂçï',
      icon: 'icon-playlist-play',
    },
  };

  @Prop() private msg!: string;
  public handleMenu(key: any) {
    // remote.getCurrentWindow().maximize();
    // remote.getCurrentWindow().minimize();
    // remote.getCurrentWindow().unmaximize();
    // remote.getCurrentWindow().restore();
    const mainWindow = remote.getCurrentWindow();
    const build: any = {
      close: () => {
        // ipcRenderer.send(MAIN_CLOSE);
        mainWindow.unmaximize();
      },
      min: () => {
        // ipcRenderer.send(MAIN_MIN);
        mainWindow.minimize();
      },
      zoom: () => {
        if (mainWindow.isFullScreen()) {
          mainWindow.setFullScreen(false);
          return;
        }
        // ipcRenderer.send(MAIN_ZOOM);
        if (mainWindow.isMaximized()) {
          mainWindow.setFullScreen(true);
        } else {
          mainWindow.maximize();
        }
      },
    };
    build[key]();
  }
  @Watch('keywords')
  public async handleSeach(keywords: any) {
    if (keywords) {
      const res = await get_search_suggest(`keywords=${keywords}`);
      if (res.code === 200) {
        this.seachList = res.result;
      }
    }
  }
  public handleGoSeach(item: any, state: any) {
    // Ê≠åÊõ≤
    if (state === 'songs') {
      const params = {
        id: item.id,
        name: item.name,
        auth: item.artists
          .map((e: any) => e.name)
          .toString()
          .split(',')
          .join('/'),
        image: item.artists[0].img1v1Url,
        duration: item.duration,
      };
      this.$store.commit('updata_music_data', params);
    }
    // Ê≠åÂçï

    if (state === 'playlists') {
      this.$router.push({
        path: '/find/music/detail',
        query: item,
      });
    }
    this.visible = false;
  }
  public showDrawer() {
    this.visible = true;
  }

  public handleBack() {
    this.$router.go(-1);
  }
}
</script>

